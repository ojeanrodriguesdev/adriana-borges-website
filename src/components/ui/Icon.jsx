/**
 * Componente de ícone consistente
 * Centraliza o uso de todos os ícones SVG
 */
export default function Icon({ 
  name, 
  category = 'ui', 
  size = 24, 
  className = '', 
  ...props 
}) {
  const iconPath = ICON_PATHS[category]?.[name];
  
  if (!iconPath) {
    console.warn(`Ícone não encontrado: ${category}/${name}`);
    return null;
  }

  // Use a switch statement to avoid circular dependency
  switch (name) {
    case 'laser':
      return <LaserIcon size={size} className={className} {...props} />;
    case 'shield':
      return <ShieldIcon size={size} className={className} {...props} />;
    case 'clock':
      return <ClockIcon size={size} className={className} {...props} />;
    case 'whatsapp':
      return <WhatsAppIcon size={size} className={className} {...props} />;
    case 'instagram':
      return <InstagramIcon size={size} className={className} {...props} />;
    case 'phone':
      return <PhoneIcon size={size} className={className} {...props} />;
    case 'email':
      return <EmailIcon size={size} className={className} {...props} />;
    case 'star':
      return <StarIcon size={size} className={className} {...props} />;
    case 'check':
      return <CheckIcon size={size} className={className} {...props} />;
    case 'arrowDown':
      return <ArrowDownIcon size={size} className={className} {...props} />;
    case 'close':
      return <CloseIcon size={size} className={className} {...props} />;
    default:
      // Fallback to img if no specific component is found
      return (
        <img
          src={iconPath}
          alt={`${name} icon`}
          width={size}
          height={size}
          className={className}
          {...props}
        />
      );
  }
}
